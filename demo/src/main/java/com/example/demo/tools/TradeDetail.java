package com.example.demo.tools;

public class TradeDetail {
	private String ch;
	private TradeDatas tick;
	private String ts;
	public String getCh() {
		return ch;
	}
	public void setCh(String ch) {
		this.ch = ch;
	}
	public TradeDatas getTick() {
		return tick;
	}
	public void setTick(TradeDatas tick) {
		this.tick = tick;
	}
	public String getTs() {
		return ts;
	}
	public void setTs(String ts) {
		this.ts = ts;
	}
}


