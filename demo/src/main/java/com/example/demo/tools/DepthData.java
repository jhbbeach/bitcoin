package com.example.demo.tools;

public class DepthData {
	private String ch;
	private MarketDatas tick;
	private String ts;
	public String getTs() {
		return ts;
	}
	public void setTs(String ts) {
		this.ts = ts;
	}
	public String getCh() {
		return ch;
	}
	public void setCh(String ch) {
		this.ch = ch;
	}
	public MarketDatas getTick() {
		return tick;
	}
	public void setTick(MarketDatas tick) {
		this.tick = tick;
	}
	
}


